#!/bin/bash

source "main.sh" 2>/dev/null

#-------------- Проверка параметров запуска --------------

args=$# # Количество аргументов, переданных в скрипт
id=$1 # Передаем ID РЛС

if (( args != 1 )) || (( id > 2 )) || (( id < 0 ))
then
	echo "Параметры запуска системы заданы неверно(1)!"
	exit 1
fi
